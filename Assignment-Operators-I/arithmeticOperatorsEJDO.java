//Task 1: Arithmetic Operations
public class arithmeticOperatorsEJDO {
    public static void main (String[] args) {
        float a = 20.55f, b = 10.15f; //declare and initialize floating-point numbers

        //perform arithmetic operations and display results with 2 decimal places
        System.out.printf("Addition: %.2f\n", (a + b));
        System.out.printf("Subtraction: %.2f\n", (a - b));
        System.out.printf("Multiplication: %.2f\n", (a * b));
        System.out.printf("Division: %.2f\n", (a / b));
    }
}